    private static string BuildPdfPageContent(
        ReportExecutionResponseDTO report,
        string generatedBy,
        string institutionName,
        string logoText,
        string filterSummary,
        List<ReportSummaryMetricDTO> summaryMetrics,
        int pageNumber,
        int totalPages,
        List<Dictionary<string, object?>> rows,
        List<decimal> columnWidths,
        decimal pageWidth,
        decimal pageHeight,
        decimal margin,
        decimal tableTop,
        decimal rowHeight)
    {
        var sb = new StringBuilder();
        DrawFilledRect(sb, margin, pageHeight - 96m, pageWidth - (margin * 2), 56m, "0.09 0.18 0.34");
        DrawFilledRect(sb, margin + 12m, pageHeight - 82m, 46m, 28m, "0.11 0.30 0.85");
        DrawText(sb, "F2", 12m, margin + 20m, pageHeight - 64m, logoText, "1 1 1");
        DrawText(sb, "F2", 20m, margin + 72m, pageHeight - 58m, institutionName, "1 1 1");
        DrawText(sb, "F1", 10m, margin + 72m, pageHeight - 76m, "Enterprise reporting export", "0.88 0.92 0.98");

        DrawText(sb, "F2", 18m, margin, pageHeight - 122m, report.ReportName, "0.05 0.09 0.16");
        DrawText(sb, "F1", 10m, margin, pageHeight - 140m, $"{report.Category} | {report.SubCategory}", "0.36 0.43 0.51");
        DrawText(sb, "F1", 10m, pageWidth - 220m, pageHeight - 122m, $"Generated {report.GeneratedAt:dd MMM yyyy HH:mm} UTC", "0.36 0.43 0.51");
        DrawText(sb, "F1", 10m, pageWidth - 220m, pageHeight - 140m, $"Prepared by {generatedBy}", "0.36 0.43 0.51");

        var metricCount = Math.Max(1, summaryMetrics.Count);
        var metricBoxWidth = (pageWidth - (margin * 2) - ((metricCount - 1) * 8m)) / metricCount;
        var metricY = pageHeight - 185m;
        for (var index = 0; index < metricCount; index++)
        {
            var metric = summaryMetrics.Count > index ? summaryMetrics[index] : new ReportSummaryMetricDTO { Label = "Rows", Value = report.Rows.Count.ToString("N0", CultureInfo.InvariantCulture) };
            var x = margin + (index * (metricBoxWidth + 8m));
            DrawFilledRect(sb, x, metricY, metricBoxWidth, 36m, "0.96 0.98 1");
            DrawText(sb, "F1", 8.5m, x + 8m, metricY + 24m, metric.Label, "0.29 0.33 0.38");
            DrawText(sb, "F2", 12m, x + 8m, metricY + 10m, Truncate(metric.Value, 22), "0.05 0.09 0.16");
        }

        DrawText(sb, "F1", 9m, margin, pageHeight - 210m, $"Filters: {Truncate(filterSummary, 115)}", "0.29 0.33 0.38");

        var tableY = pageHeight - tableTop;
        DrawFilledRect(sb, margin, tableY, pageWidth - (margin * 2), rowHeight, "0.11 0.30 0.85");

        var xCursor = margin;
        for (var columnIndex = 0; columnIndex < report.Columns.Count; columnIndex++)
        {
            DrawText(sb, "F2", 9m, xCursor + 4m, tableY + 7m, Truncate(HumanizeColumnName(report.Columns[columnIndex]), MaxCharsForWidth(columnWidths[columnIndex])), "1 1 1");
            DrawStrokeRect(sb, xCursor, tableY, columnWidths[columnIndex], rowHeight, "0.74 0.84 0.98");
            xCursor += columnWidths[columnIndex];
        }

        var yCursor = tableY - rowHeight;
        for (var rowIndex = 0; rowIndex < rows.Count; rowIndex++)
        {
            if (rowIndex % 2 == 0)
            {
                DrawFilledRect(sb, margin, yCursor, pageWidth - (margin * 2), rowHeight, "0.98 0.99 1");
            }

            xCursor = margin;
            for (var columnIndex = 0; columnIndex < report.Columns.Count; columnIndex++)
            {
                var column = report.Columns[columnIndex];
                var value = rows[rowIndex].TryGetValue(column, out var raw) ? raw : null;
                var text = Truncate(FormatValue(value), MaxCharsForWidth(columnWidths[columnIndex]));
                var textX = IsNumeric(value) ? xCursor + columnWidths[columnIndex] - 4m - (text.Length * 4.1m) : xCursor + 4m;
                DrawText(sb, "F1", 8.5m, textX, yCursor + 7m, text, "0.11 0.17 0.24");
                DrawStrokeRect(sb, xCursor, yCursor, columnWidths[columnIndex], rowHeight, "0.85 0.89 0.94");
                xCursor += columnWidths[columnIndex];
            }

            yCursor -= rowHeight;
        }

        DrawText(sb, "F1", 9m, margin, 18m, "Confidential banking information. Prepared for presentation use.", "0.45 0.49 0.56");
        DrawText(sb, "F1", 9m, pageWidth - 92m, 18m, $"Page {pageNumber}/{totalPages}", "0.45 0.49 0.56");
        return sb.ToString();
    }

    private static List<decimal> CalculatePdfColumnWidths(List<string> columns, List<Dictionary<string, object?>> rows, decimal availableWidth)
    {
        var weights = columns.Select(column =>
        {
            var headerLength = HumanizeColumnName(column).Length;
            var valueLength = rows.Select(row => row.TryGetValue(column, out var value) ? FormatValue(value).Length : 0).DefaultIfEmpty(0).Max();
            return Math.Max(10, Math.Min(30, Math.Max(headerLength, valueLength)));
        }).ToList();

        var totalWeight = weights.Sum();
        var widths = weights.Select(weight => decimal.Round(availableWidth * (weight / totalWeight), 2, MidpointRounding.AwayFromZero)).ToList();
        for (var index = 0; index < widths.Count; index++)
        {
            widths[index] = Math.Clamp(widths[index], 70m, 180m);
        }

        var adjustedTotal = widths.Sum();
        if (widths.Count > 0)
        {
            widths[^1] += availableWidth - adjustedTotal;
        }

        return widths;
    }

    private static int MaxCharsForWidth(decimal width) => Math.Max(8, (int)Math.Floor((double)(width / 5.4m)));

    private static void DrawFilledRect(StringBuilder sb, decimal x, decimal y, decimal width, decimal height, string rgb)
    {
        sb.AppendLine($"{rgb} rg");
        sb.AppendLine($"{Fmt(x)} {Fmt(y)} {Fmt(width)} {Fmt(height)} re f");
    }

    private static void DrawStrokeRect(StringBuilder sb, decimal x, decimal y, decimal width, decimal height, string rgb)
    {
        sb.AppendLine($"{rgb} RG");
        sb.AppendLine("0.5 w");
        sb.AppendLine($"{Fmt(x)} {Fmt(y)} {Fmt(width)} {Fmt(height)} re S");
    }

    private static void DrawText(StringBuilder sb, string font, decimal size, decimal x, decimal y, string text, string rgb)
    {
        sb.AppendLine("BT");
        sb.AppendLine($"{rgb} rg");
        sb.AppendLine($"/{font} {Fmt(size)} Tf");
        sb.AppendLine($"1 0 0 1 {Fmt(x)} {Fmt(y)} Tm");
        sb.AppendLine($"({EscapePdf(text)}) Tj");
        sb.AppendLine("ET");
    }

    private static string Fmt(decimal value) => value.ToString("0.##", CultureInfo.InvariantCulture);

    private static XElement Row(int rowIndex, params XElement[] cells) => new(SpreadsheetNs + "row", new XAttribute("r", rowIndex), cells);

    private static XElement TextCell(int columnIndex, int rowIndex, string value, int styleIndex = 0) =>
        new(SpreadsheetNs + "c",
            new XAttribute("r", CellReference(columnIndex, rowIndex)),
            new XAttribute("s", styleIndex),
            new XAttribute("t", "inlineStr"),
            new XElement(SpreadsheetNs + "is", new XElement(SpreadsheetNs + "t", value ?? string.Empty)));

    private static XElement LogoCell(int columnIndex, int rowIndex, string value) => TextCell(columnIndex, rowIndex, value, 5);

    private static XElement NumberCell(int columnIndex, int rowIndex, decimal value, int styleIndex) =>
        new(SpreadsheetNs + "c",
            new XAttribute("r", CellReference(columnIndex, rowIndex)),
            new XAttribute("s", styleIndex),
            new XElement(SpreadsheetNs + "v", value.ToString(CultureInfo.InvariantCulture)));

    private static void AddEntry(ZipArchive archive, string name, string content)
    {
        var entry = archive.CreateEntry(name, CompressionLevel.Fastest);
        using var writer = new StreamWriter(entry.Open(), new UTF8Encoding(false));
        writer.Write(content);
    }

    private static string FilterSummary(ReportExecutionResponseDTO report) =>
        report.AppliedFilters.Count == 0 ? "None" : string.Join(" | ", report.AppliedFilters.Select(kv => $"{HumanizeColumnName(kv.Key)}={kv.Value}"));

    private static string GetLogoText(string institutionName)
    {
        var tokens = institutionName.Split(new[] { ' ', '-', '_' }, StringSplitOptions.RemoveEmptyEntries).Take(3).Select(token => char.ToUpperInvariant(token[0]));
        var logo = string.Concat(tokens);
        return string.IsNullOrWhiteSpace(logo) ? "BI" : logo;
    }

    private static string HumanizeColumnName(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
        {
            return string.Empty;
        }

        var normalized = new StringBuilder();
        normalized.Append(char.ToUpperInvariant(value[0]));
        for (var index = 1; index < value.Length; index++)
        {
            var current = value[index];
            var previous = value[index - 1];
            if ((char.IsUpper(current) && char.IsLower(previous)) || current is '_' or '-')
            {
                normalized.Append(' ');
            }

            normalized.Append(current is '_' or '-' ? ' ' : current);
        }

        return normalized.ToString();
    }

    private static bool TryGetDateSerial(object? value, out decimal serial)
    {
        serial = 0m;
        switch (value)
        {
            case DateTime dateTime:
                serial = Convert.ToDecimal(dateTime.ToOADate(), CultureInfo.InvariantCulture);
                return true;
            case DateOnly dateOnly:
                serial = Convert.ToDecimal(dateOnly.ToDateTime(TimeOnly.MinValue).ToOADate(), CultureInfo.InvariantCulture);
                return true;
            default:
                return false;
        }
    }

    private static bool IsNumeric(object? value) => value is byte or short or int or long or float or double or decimal;

    private static string EscapeCsv(string value)
    {
        var input = value ?? string.Empty;
        if (input.Contains(',') || input.Contains('"') || input.Contains('\n'))
        {
            return '"' + input.Replace("\"", "\"\"") + '"';
        }

        return input;
    }

    private static string FormatValue(object? value) => value switch
    {
        null => string.Empty,
        DateTime dateTime => dateTime.ToString("yyyy-MM-dd HH:mm:ss", CultureInfo.InvariantCulture),
        DateOnly dateOnly => dateOnly.ToString("yyyy-MM-dd", CultureInfo.InvariantCulture),
        decimal number => number.ToString("0.00", CultureInfo.InvariantCulture),
        double number => number.ToString("0.00", CultureInfo.InvariantCulture),
        float number => number.ToString("0.00", CultureInfo.InvariantCulture),
        _ => Convert.ToString(value, CultureInfo.InvariantCulture) ?? string.Empty
    };

    private static string EscapePdf(string value) => (value ?? string.Empty).Replace("\\", "\\\\").Replace("(", "\\(").Replace(")", "\\)");
    private static string Truncate(string value, int max) => string.IsNullOrEmpty(value) || value.Length <= max ? value : value[..Math.Max(0, max - 1)] + "...";
    private static string SanitizeFileName(string value) => string.Concat(value.Select(ch => Path.GetInvalidFileNameChars().Contains(ch) ? '_' : ch));

    private static string CellReference(int columnIndex, int rowIndex)
    {
        var dividend = columnIndex;
        var columnName = string.Empty;
        while (dividend > 0)
        {
            var modulo = (dividend - 1) % 26;
            columnName = Convert.ToChar(65 + modulo) + columnName;
            dividend = (dividend - modulo) / 26;
        }

        return columnName + rowIndex;
    }
