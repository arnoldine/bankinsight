    public (byte[] Content, string ContentType, string FileName) Export(ReportExecutionResponseDTO report, string format, string generatedBy, string institutionName)
    {
        var normalized = (format ?? "csv").Trim().ToLowerInvariant();
        var safeName = SanitizeFileName($"{report.ReportCode}_{DateTime.UtcNow:yyyyMMdd_HHmmss}");

        return normalized switch
        {
            "xlsx" => (BuildXlsx(report, generatedBy, institutionName), "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet", safeName + ".xlsx"),
            "pdf" => (BuildPdf(report, generatedBy, institutionName), "application/pdf", safeName + ".pdf"),
            _ => (BuildCsv(report, generatedBy, institutionName), "text/csv", safeName + ".csv")
        };
    }

    private static byte[] BuildCsv(ReportExecutionResponseDTO report, string generatedBy, string institutionName)
    {
        var sb = new StringBuilder();
        sb.AppendLine($"Institution,{EscapeCsv(institutionName)}");
        sb.AppendLine($"Report,{EscapeCsv(report.ReportName)}");
        sb.AppendLine($"Generated At,{report.GeneratedAt:yyyy-MM-dd HH:mm:ss}");
        sb.AppendLine($"Generated By,{EscapeCsv(generatedBy)}");
        if (report.AppliedFilters.Count > 0)
        {
            sb.AppendLine($"Filters,{EscapeCsv(FilterSummary(report))}");
        }

        sb.AppendLine();
        sb.AppendLine(string.Join(",", report.Columns.Select(EscapeCsv)));
        foreach (var row in report.Rows)
        {
            sb.AppendLine(string.Join(",", report.Columns.Select(column => EscapeCsv(FormatValue(row.TryGetValue(column, out var value) ? value : null)))));
        }

        return Encoding.UTF8.GetBytes(sb.ToString());
    }

    private static byte[] BuildXlsx(ReportExecutionResponseDTO report, string generatedBy, string institutionName)
    {
        using var stream = new MemoryStream();
        using var archive = new ZipArchive(stream, ZipArchiveMode.Create, true);

        AddEntry(archive, "[Content_Types].xml", BuildContentTypesXml());
        AddEntry(archive, "_rels/.rels", BuildRootRelationshipsXml());
        AddEntry(archive, "docProps/app.xml", BuildAppPropertiesXml());
        AddEntry(archive, "docProps/core.xml", BuildCorePropertiesXml(report, generatedBy));
        AddEntry(archive, "xl/workbook.xml", BuildWorkbookXml());
        AddEntry(archive, "xl/_rels/workbook.xml.rels", BuildWorkbookRelationshipsXml());
        AddEntry(archive, "xl/styles.xml", BuildStylesXml());
        AddEntry(archive, "xl/worksheets/sheet1.xml", BuildSheetXml(report, generatedBy, institutionName));

        return stream.ToArray();
    }

    private static byte[] BuildPdf(ReportExecutionResponseDTO report, string generatedBy, string institutionName)
    {
        const decimal pageWidth = 842m;
        const decimal pageHeight = 595m;
        const decimal margin = 36m;
        const decimal rowHeight = 22m;
        const decimal tableTop = 238m;

        var logoText = GetLogoText(institutionName);
        var filterSummary = FilterSummary(report);
        var summaryMetrics = report.Summary.Take(4).ToList();
        var columnWidths = CalculatePdfColumnWidths(report.Columns, report.Rows, pageWidth - (margin * 2));
        var rowsPerPage = Math.Max(8, (int)Math.Floor((pageHeight - tableTop - 34m) / rowHeight));
        var totalPages = report.Rows.Count == 0 ? 1 : (int)Math.Ceiling(report.Rows.Count / (double)rowsPerPage);

        var objects = new List<string>
        {
            "1 0 obj << /Type /Catalog /Pages 2 0 R >> endobj",
            string.Empty,
            "3 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica >> endobj",
            "4 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >> endobj"
        };
        var pageIds = new List<int>();

        for (var pageIndex = 0; pageIndex < totalPages; pageIndex++)
        {
            var pageRows = report.Rows.Skip(pageIndex * rowsPerPage).Take(rowsPerPage).ToList();
            var content = BuildPdfPageContent(
                report,
                generatedBy,
                institutionName,
                logoText,
                filterSummary,
                summaryMetrics,
                pageIndex + 1,
                totalPages,
                pageRows,
                columnWidths,
                pageWidth,
                pageHeight,
                margin,
                tableTop,
                rowHeight);

            var contentId = objects.Count + 1;
            objects.Add($"{contentId} 0 obj << /Length {Encoding.ASCII.GetByteCount(content)} >> stream\n{content}\nendstream endobj");

            var pageId = objects.Count + 1;
            pageIds.Add(pageId);
            objects.Add($"{pageId} 0 obj << /Type /Page /Parent 2 0 R /MediaBox [0 0 {pageWidth.ToString(CultureInfo.InvariantCulture)} {pageHeight.ToString(CultureInfo.InvariantCulture)}] /Resources << /Font << /F1 3 0 R /F2 4 0 R >> >> /Contents {contentId} 0 R >> endobj");
        }

        objects[1] = $"2 0 obj << /Type /Pages /Kids [{string.Join(" ", pageIds.Select(id => $"{id} 0 R"))}] /Count {pageIds.Count} >> endobj";

        var pdf = new StringBuilder();
        pdf.AppendLine("%PDF-1.4");
        var offsets = new List<int>();
        foreach (var obj in objects)
        {
            offsets.Add(Encoding.ASCII.GetByteCount(pdf.ToString()));
            pdf.AppendLine(obj);
        }

        var xrefOffset = Encoding.ASCII.GetByteCount(pdf.ToString());
        pdf.AppendLine("xref");
        pdf.AppendLine($"0 {objects.Count + 1}");
        pdf.AppendLine("0000000000 65535 f ");
        foreach (var offset in offsets)
        {
            pdf.AppendLine(offset.ToString("D10", CultureInfo.InvariantCulture) + " 00000 n ");
        }

        pdf.AppendLine($"trailer << /Size {objects.Count + 1} /Root 1 0 R >>");
        pdf.AppendLine("startxref");
        pdf.AppendLine(xrefOffset.ToString(CultureInfo.InvariantCulture));
        pdf.AppendLine("%%EOF");

        return Encoding.ASCII.GetBytes(pdf.ToString());
    }
    private static string BuildSheetXml(ReportExecutionResponseDTO report, string generatedBy, string institutionName)
    {
        var rows = new List<XElement>();
        var merges = new List<string> { "A2:F2", "A3:F3", "A4:F4", "A5:F5", "A6:F6", "A7:F7", "A9:F9" };
        var currentRow = 1;
        var logoText = GetLogoText(institutionName);

        rows.Add(Row(currentRow++, LogoCell(1, 1, logoText), TextCell(2, 1, "Presentation Export", 3)));
        rows.Add(Row(currentRow++, TextCell(1, 2, institutionName, 2)));
        rows.Add(Row(currentRow++, TextCell(1, 3, report.ReportName, 1)));
        rows.Add(Row(currentRow++, TextCell(1, 4, $"{report.Category} | {report.SubCategory}", 3)));
        rows.Add(Row(currentRow++, TextCell(1, 5, $"Generated on {report.GeneratedAt:dd MMM yyyy HH:mm} UTC", 3)));
        rows.Add(Row(currentRow++, TextCell(1, 6, $"Prepared by {generatedBy}", 3)));
        rows.Add(Row(currentRow++, TextCell(1, 7, $"Applied filters: {FilterSummary(report)}", 8)));
        rows.Add(Row(currentRow++));
        rows.Add(Row(currentRow++, TextCell(1, 9, "Report Data", 5)));
        rows.Add(Row(currentRow++, report.Columns.Select((column, index) => TextCell(index + 1, currentRow - 1, HumanizeColumnName(column), 4)).ToArray()));

        foreach (var row in report.Rows)
        {
            rows.Add(Row(currentRow, report.Columns.Select((column, index) => BuildDataCell(index + 1, currentRow, row.TryGetValue(column, out var value) ? value : null)).ToArray()));
            currentRow++;
        }

        if (report.Rows.Count > 0)
        {
            var totals = BuildTotalsRow(report);
            if (totals.Any(x => !string.IsNullOrWhiteSpace(x.Value)))
            {
                rows.Add(Row(currentRow++, totals.Select((cell, index) => BuildStyledValueCell(index + 1, currentRow - 1, cell.Value, cell.Style)).ToArray()));
            }
        }

        if (report.Summary.Count > 0)
        {
            rows.Add(Row(currentRow++));
            rows.Add(Row(currentRow++, TextCell(1, currentRow - 1, "Executive Summary", 5)));
            foreach (var metric in report.Summary.Take(8))
            {
                rows.Add(Row(currentRow, TextCell(1, currentRow, metric.Label, 3), TextCell(2, currentRow, metric.Value, 9), TextCell(3, currentRow, metric.Helper ?? string.Empty, 8)));
                currentRow++;
            }
        }

        if (report.ValidationMessages.Count > 0)
        {
            rows.Add(Row(currentRow++));
            rows.Add(Row(currentRow++, TextCell(1, currentRow - 1, "Validation Notes", 5)));
            foreach (var message in report.ValidationMessages.Take(6))
            {
                rows.Add(Row(currentRow++, TextCell(1, currentRow - 1, message, 8)));
            }
        }

        var worksheet = new XDocument(
            new XDeclaration("1.0", "UTF-8", "yes"),
            new XElement(
                SpreadsheetNs + "worksheet",
                new XAttribute(XNamespace.Xmlns + "r", OfficeRelNs.NamespaceName),
                BuildColumnsXml(CalculateExcelColumnWidths(report)),
                new XElement(
                    SpreadsheetNs + "sheetViews",
                    new XElement(
                        SpreadsheetNs + "sheetView",
                        new XAttribute("workbookViewId", 0),
                        new XElement(SpreadsheetNs + "pane", new XAttribute("ySplit", 10), new XAttribute("topLeftCell", "A11"), new XAttribute("state", "frozen"), new XAttribute("activePane", "bottomLeft")))),
                new XElement(SpreadsheetNs + "sheetFormatPr", new XAttribute("defaultRowHeight", 18)),
                new XElement(SpreadsheetNs + "sheetData", rows),
                new XElement(SpreadsheetNs + "mergeCells", new XAttribute("count", merges.Count), merges.Select(item => new XElement(SpreadsheetNs + "mergeCell", new XAttribute("ref", item)))),
                report.Columns.Count > 0 ? new XElement(SpreadsheetNs + "autoFilter", new XAttribute("ref", $"A10:{CellReference(report.Columns.Count, Math.Max(10, 9 + report.Rows.Count))}")) : null,
                new XElement(SpreadsheetNs + "pageSetup", new XAttribute("orientation", report.Columns.Count > 6 ? "landscape" : "portrait"), new XAttribute("paperSize", 9)),
                new XElement(SpreadsheetNs + "pageMargins", new XAttribute("left", 0.35), new XAttribute("right", 0.35), new XAttribute("top", 0.55), new XAttribute("bottom", 0.55), new XAttribute("header", 0.2), new XAttribute("footer", 0.2))));

        return worksheet.ToString(SaveOptions.DisableFormatting);
    }

    private static XElement BuildColumnsXml(IEnumerable<double> widths) =>
        new(SpreadsheetNs + "cols",
            widths.Select((width, index) => new XElement(
                SpreadsheetNs + "col",
                new XAttribute("min", index + 1),
                new XAttribute("max", index + 1),
                new XAttribute("width", width.ToString("0.##", CultureInfo.InvariantCulture)),
                new XAttribute("customWidth", 1))));

    private static IEnumerable<double> CalculateExcelColumnWidths(ReportExecutionResponseDTO report)
    {
        var widths = new List<double>();
        foreach (var column in report.Columns)
        {
            var longest = Math.Max(HumanizeColumnName(column).Length, report.Rows.Select(row => row.TryGetValue(column, out var value) ? FormatValue(value).Length : 0).DefaultIfEmpty(0).Max());
            widths.Add(Math.Clamp(longest + 4, 12, 28));
        }

        while (widths.Count < 6)
        {
            widths.Add(18);
        }

        return widths;
    }

    private static List<(string Value, int Style)> BuildTotalsRow(ReportExecutionResponseDTO report)
    {
        var result = new List<(string Value, int Style)>();
        var labelPlaced = false;
        foreach (var column in report.Columns)
        {
            var values = report.Rows.Select(row => row.TryGetValue(column, out var value) ? value : null).ToList();
            var numericValues = values.Where(IsNumeric).Select(value => Convert.ToDecimal(value, CultureInfo.InvariantCulture)).ToList();
            if (numericValues.Count == values.Count && numericValues.Count > 0)
            {
                if (!labelPlaced)
                {
                    result.Add(("Total", 5));
                    labelPlaced = true;
                }
                else
                {
                    result.Add((decimal.Round(numericValues.Sum(), 2, MidpointRounding.AwayFromZero).ToString("0.00", CultureInfo.InvariantCulture), 9));
                }
            }
            else
            {
                result.Add((labelPlaced ? string.Empty : "Total", labelPlaced ? 8 : 5));
                labelPlaced = true;
            }
        }

        return result;
    }

    private static XElement BuildDataCell(int columnIndex, int rowIndex, object? value)
    {
        if (TryGetDateSerial(value, out var serial))
        {
            return NumberCell(columnIndex, rowIndex, serial, 7);
        }

        if (IsNumeric(value))
        {
            return NumberCell(columnIndex, rowIndex, Convert.ToDecimal(value, CultureInfo.InvariantCulture), 6);
        }

        return TextCell(columnIndex, rowIndex, FormatValue(value), 8);
    }

    private static XElement BuildStyledValueCell(int columnIndex, int rowIndex, string value, int style)
    {
        if (decimal.TryParse(value, NumberStyles.Any, CultureInfo.InvariantCulture, out var numeric))
        {
            return NumberCell(columnIndex, rowIndex, numeric, style);
        }

        return TextCell(columnIndex, rowIndex, value, style);
    }

